class Bike {
  final String id;
  final String name;
  final String brand;
  final double price;
  final String image;
  final String description;
  final double rating;
  final List<String> colors;
  final String category;

  Bike({
    required this.id,
    required this.name,
    required this.brand,
    required this.price,
    required this.image,
    required this.description,
    required this.rating,
    required this.colors,
    required this.category,
  });
}
